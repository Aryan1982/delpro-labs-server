const Project = require("../models/Project");
const User = require("../models/User");

// Get Projects (role-based)
exports.getProjects = async (req, res) => {
  try {
    const { role, id: userId } = req.user;
    const { status, priority, search, page = 1, limit = 20 } = req.query;

    // Build query
    let query = {};

    // Role-based filtering
    if (role === "client") {
      query.clientId = userId;
    }
    // super_admin and internal_staff see all projects

    // Additional filters
    if (status) query.status = status;
    if (priority) query.priority = priority;
    if (search) {
      query.$text = { $search: search };
    }

    // Execute query with pagination
    const projects = await Project.find(query)
      .populate("clientId", "name email")
      .populate("assignedStaffId", "name email")
      .sort({ createdAt: -1 })
      .limit(limit * 1)
      .skip((page - 1) * limit)
      .lean();

    const count = await Project.countDocuments(query);

    // Transform response
    const transformedProjects = projects.map((project) => {
      // Use projectId if available, otherwise fallback to _id
      const projectId = project.projectId || project._id?.toString();
      return {
        id: projectId,
        title: project.title,
        description: project.description,
        status: project.status,
        priority: project.priority,
        testType: project.testType,
        clientId: project.clientId?._id,
        client: project.clientId
          ? {
              id: project.clientId._id,
              name: project.clientId.name,
              email: project.clientId.email,
            }
          : null,
        assignedStaffId: project.assignedStaffId?._id,
        assignedStaffName: project.assignedStaffId?.name,
        dueDate: project.dueDate,
        createdAt: project.createdAt,
        updatedAt: project.updatedAt,
      };
    });

    return res.status(200).json({
      success: true,
      data: transformedProjects,
      pagination: {
        total: count,
        page: parseInt(page),
        limit: parseInt(limit),
        totalPages: Math.ceil(count / limit),
      },
    });
  } catch (error) {
    console.error("Get projects error:", error);
    return res.status(500).json({
      success: false,
      message: "Failed to fetch projects",
    });
  }
};

// Get Single Project
exports.getProject = async (req, res) => {
  try {
    const { id } = req.params;
    const { role, id: userId } = req.user;

    // Try to find project by projectId first, then by _id
    let project = await Project.findOne({ projectId: id })
      .populate("clientId", "name email")
      .populate("assignedStaffId", "name email");

    if (!project) {
      project = await Project.findById(id)
        .populate("clientId", "name email")
        .populate("assignedStaffId", "name email");
    }

    if (!project) {
      return res.status(404).json({
        success: false,
        message: "Project not found",
      });
    }

    // Authorization check
    if (role === "client" && project.clientId._id.toString() !== userId) {
      return res.status(403).json({
        success: false,
        message: "Access denied",
      });
    }

    // Use projectId if available, otherwise fallback to _id
    const projectId = project.projectId || project._id?.toString();

    return res.status(200).json({
      success: true,
      data: {
        id: projectId,
        title: project.title,
        description: project.description,
        status: project.status,
        priority: project.priority,
        testType: project.testType,
        clientId: project.clientId._id,
        client: {
          id: project.clientId._id,
          name: project.clientId.name,
          email: project.clientId.email,
        },
        assignedStaffId: project.assignedStaffId?._id,
        assignedStaffName: project.assignedStaffId?.name,
        dueDate: project.dueDate,
        createdAt: project.createdAt,
        updatedAt: project.updatedAt,
      },
    });
  } catch (error) {
    console.error("Get project error:", error);
    return res.status(500).json({
      success: false,
      message: "Failed to fetch project",
    });
  }
};

// Create Project
exports.createProject = async (req, res) => {
  try {
    const {
      title,
      description,
      clientId,
      testType,
      priority,
      status,
      dueDate,
    } = req.body;

    // Validate client exists
    const client = await User.findById(clientId);
    if (!client || client.role !== "client") {
      return res.status(400).json({
        success: false,
        message: "Invalid client",
      });
    }

    // Create project (projectId auto-generated by pre-save hook)
    const project = new Project({
      title,
      description,
      clientId,
      testType,
      priority: priority || "medium",
      status: status || "pending",
      dueDate: dueDate || null,
    });

    await project.save();
    await project.populate("clientId", "name email");

    return res.status(201).json({
      success: true,
      message: "Project created successfully",
      data: {
        id: project.projectId,
        title: project.title,
        description: project.description,
        status: project.status,
        priority: project.priority,
        testType: project.testType,
        clientId: project.clientId._id,
        clientName: project.clientId.name,
        dueDate: project.dueDate,
        createdAt: project.createdAt,
        updatedAt: project.updatedAt,
      },
    });
  } catch (error) {
    console.error("Create project error:", error);
    return res.status(500).json({
      success: false,
      message: "Failed to create project",
    });
  }
};

// Update Project
exports.updateProject = async (req, res) => {
  try {
    const { id } = req.params;
    const updates = req.body;

    // Don't allow updating projectId or clientId
    delete updates.projectId;
    delete updates.clientId;

    // Try to find project by projectId first, then by _id
    let project = await Project.findOne({ projectId: id });
    if (!project) {
      project = await Project.findById(id);
    }

    if (!project) {
      return res.status(404).json({
        success: false,
        message: "Project not found",
      });
    }

    const updatedProject = await Project.findByIdAndUpdate(
      project._id,
      updates,
      {
        new: true,
        runValidators: true,
      },
    )
      .populate("clientId", "name email")
      .populate("assignedStaffId", "name email");

    // Use projectId if available, otherwise fallback to _id
    const responseProjectId =
      updatedProject.projectId || updatedProject._id?.toString();

    return res.status(200).json({
      success: true,
      message: "Project updated successfully",
      data: {
        id: responseProjectId,
        title: updatedProject.title,
        description: updatedProject.description,
        status: updatedProject.status,
        priority: updatedProject.priority,
        dueDate: updatedProject.dueDate,
        updatedAt: updatedProject.updatedAt,
      },
    });
  } catch (error) {
    console.error("Update project error:", error);
    return res.status(500).json({
      success: false,
      message: "Failed to update project",
    });
  }
};

// Delete Project
exports.deleteProject = async (req, res) => {
  try {
    const { id } = req.params;

    // Try to find project by projectId first, then by _id
    let project = await Project.findOne({ projectId: id });
    if (!project) {
      project = await Project.findById(id);
    }

    if (!project) {
      return res.status(404).json({
        success: false,
        message: "Project not found",
      });
    }

    await Project.findByIdAndDelete(project._id);

    return res.status(200).json({
      success: true,
      message: "Project deleted successfully",
    });
  } catch (error) {
    console.error("Delete project error:", error);
    return res.status(500).json({
      success: false,
      message: "Failed to delete project",
    });
  }
};

// Assign Staff
exports.assignStaff = async (req, res) => {
  try {
    const { id } = req.params;
    const { staffId } = req.body;

    // Validate staff exists and is internal_staff
    const staff = await User.findById(staffId);
    if (!staff || staff.role !== "internal_staff") {
      return res.status(400).json({
        success: false,
        message: "Invalid staff member",
      });
    }

    // Try to find project by projectId first, then by _id
    let project = await Project.findOne({ projectId: id });
    if (!project) {
      project = await Project.findById(id);
    }

    if (!project) {
      return res.status(404).json({
        success: false,
        message: "Project not found",
      });
    }

    const updatedProject = await Project.findByIdAndUpdate(
      project._id,
      {
        assignedStaffId: staffId,
        status: "in_progress", // Auto-update status
      },
      { new: true },
    ).populate("assignedStaffId", "name email");

    // Use projectId if available, otherwise fallback to _id
    const responseProjectId =
      updatedProject.projectId || updatedProject._id?.toString();

    return res.status(200).json({
      success: true,
      message: "Staff assigned successfully",
      data: {
        id: responseProjectId,
        assignedStaffId: updatedProject.assignedStaffId._id,
        assignedStaffName: updatedProject.assignedStaffId.name,
        status: updatedProject.status,
        updatedAt: updatedProject.updatedAt,
      },
    });
  } catch (error) {
    console.error("Assign staff error:", error);
    return res.status(500).json({
      success: false,
      message: "Failed to assign staff",
    });
  }
};
